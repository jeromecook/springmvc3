package model;

public class ProcessTO {
      private int Length;
      private int Width;
      private int Square;
      
	public int getSquare() {
		return Square;
	}
	public void setSquare(int square) {
		Square = square;
	}
	public int getLength() {
		return Length;
	}
	public void setLength(int length) {
		Length = length;
	}
	public int getWidth() {
		return Width;
	}
	public void setWidth(int width) {
		Width = width;
	}
      
}
